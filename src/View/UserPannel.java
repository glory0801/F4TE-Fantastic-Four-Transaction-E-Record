    private void userSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userSaveButtonActionPerformed
        try {
            if (!(userPhoneField.getText().matches("[0-9]*"))) {
                JOptionPane.showMessageDialog(
                    this, "Phone Number Must Be Numric Digits",
                    "Invalid Data Type",
                    JOptionPane.ERROR_MESSAGE);
            } else if (!(userEmailField.getText().matches("[A-Za-z0-9-_.]+@[A-Za-z0-9-_]+[.][a-zA-Z]{2,6}"))) {
                JOptionPane.showMessageDialog(
                    this, "Invalid Email Adress",
                    "Invalid Entry",
                    JOptionPane.ERROR_MESSAGE);
            } else {

                long flag1 = userController.update("=", "accountId", userID, "phone", userPhoneField.getText());
                long flag2 = userController.update("=", "accountId", userID, "email", userEmailField.getText());

                userPhoneField.setEditable(false);
                userEmailField.setEditable(false);

                if (flag1 >= 1 && flag2 >= 1) {
                    JOptionPane.showMessageDialog(
                        this, "Changes Saved",
                        "Scucess",
                        JOptionPane.INFORMATION_MESSAGE);

                    
                    File logs = new File("src/util/logs.txt");
                    if (!logs.getParentFile().exists()) {
                        logs.getParentFile().mkdirs(); // Buat direktori jika belum ada
                    }
                    if (!logs.exists()) {
                        logs.createNewFile();
                    }
                
                    String newLine = System.getProperty("line.separator");
                    FileOutputStream fos = new FileOutputStream(logs, true);
                    OutputStreamWriter osw = new OutputStreamWriter(fos);
                    PrintWriter pw = new PrintWriter(osw);

                    String timeStamp = new SimpleDateFormat("yyyy/MM/dd/ HH:mm:ss").format(Calendar.getInstance().getTime());
                    pw.append("Account With ID : " + userID + " Changed His Info @ " + timeStamp + newLine);
                    pw.close();
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(UserPannel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_userSaveButtonActionPerformed

