    private void userSavePassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userSavePassButtonActionPerformed
    try {
        String userInput = userOldPassField.getText();

        List<User> users = userController.search("=", "accountId", userID);
        User user = users.get(0);
        String old_password = user.getPassword();

        if (userOldPassField.getText().equals("")
                || userNewPassField.getText().equals("")
                || userConfirmPassField.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this, "Please Fill All Required Fields",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!(userNewPassField.getText().equals(userConfirmPassField.getText()))) {
            JOptionPane.showMessageDialog(
                    this, "Passwords Don't Match",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!(userInput.equals(old_password))) {
            JOptionPane.showMessageDialog(
                    this, "Wrong Password",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {

            String newPassword = userNewPassField.getText();

            long flag = userController.update("=", "accountId", userID, "password", newPassword);

            if (flag >= 1) {
                JOptionPane.showMessageDialog(
                        this, "Password Was Changed Please Re-Login",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);
                userOldPassField.setText("");
                userNewPassField.setText("");
                userConfirmPassField.setText("");

                UserLoginWindow userLoginWindow = new UserLoginWindow(this.mainPage);
                this.setVisible(false);
                userLoginWindow.setVisible(true);
                
                File logs = new File("src/util/logs.txt");
                if (!logs.getParentFile().exists()) {
                    logs.getParentFile().mkdirs(); // Buat direktori jika belum ada
                }
                if (!logs.exists()) {
                    logs.createNewFile();
                }

                String newLine = System.getProperty("line.separator");
                FileOutputStream fos = new FileOutputStream(logs, true);
                OutputStreamWriter osw = new OutputStreamWriter(fos);
                PrintWriter pw = new PrintWriter(osw);

                String timeStamp = new SimpleDateFormat("yyyy/MM/dd/ HH:mm:ss").format(Calendar.getInstance().getTime());
                pw.append("Account With ID : " + userID + " Changed His Password @ " + timeStamp + newLine);
                pw.close();
            }
        }
    } catch (IOException ex) {
        Logger.getLogger(UserPannel.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_userSavePassButtonActionPerformed

